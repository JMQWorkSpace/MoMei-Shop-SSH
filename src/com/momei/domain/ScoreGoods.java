package com.momei.domain;

public class ScoreGoods {
	
	private int scoreGoodsId ;
	private String scoreGoodsName ;
	private double price;
	private String pic;
	private String unit;
	private int count;
	private String content;
	private String statu;
	public int getScoreGoodsId() {
		return scoreGoodsId;
	}
	public void setScoreGoodsId(int scoreGoodsId) {
		this.scoreGoodsId = scoreGoodsId;
	}
	public String getScoreGoodsName() {
		return scoreGoodsName;
	}
	public void setScoreGoodsName(String scoreGoodsName) {
		this.scoreGoodsName = scoreGoodsName;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getStatu() {
		return statu;
	}
	public void setStatu(String statu) {
		this.statu = statu;
	}

}
